class ErrorMessagesUtil:

    # Персоналии

    @staticmethod
    def person_not_found():
        return f"Person not found."

    # Фильмы

    @staticmethod
    def film_not_found():
        return f"Film not found."

    @staticmethod
    def films_not_found():
        return f"Films not found."

    # Жанры

    @staticmethod
    def genre_not_found():
        return f"Genre not found."

    @staticmethod
    def genres_not_found():
        return f"Genres not found."

